#!/usr/bin/env bash
# Archive Node Modules Cleanup Script
# Recovers 720MB+ by removing archived node_modules directories
# Reduces folder depth from 18 to 6 levels

set -e

# Colors for output
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
NC='\033[0m' # No Color

# Configuration
ARCHIVE_DIR="$HOME/workspace/archive"
BACKUP_DIR="$HOME/backups"
TMP_LIST="/tmp/node_modules_list.txt"
DRY_RUN=false
WITH_BACKUP=false

# Parse arguments
while [[ $# -gt 0 ]]; do
    case $1 in
        --dry-run)
            DRY_RUN=true
            shift
            ;;
        --with-backup)
            WITH_BACKUP=true
            shift
            ;;
        --help)
            echo "Usage: $0 [OPTIONS]"
            echo ""
            echo "Options:"
            echo "  --dry-run       Show what would be deleted without actually deleting"
            echo "  --with-backup   Create backup before deletion"
            echo "  --help          Show this help message"
            exit 0
            ;;
        *)
            echo "Unknown option: $1"
            echo "Use --help for usage information"
            exit 1
            ;;
    esac
done

echo -e "${GREEN}======================================"
echo "Archive Node Modules Cleanup Script"
echo -e "======================================${NC}\n"

# Check if archive directory exists
if [ ! -d "$ARCHIVE_DIR" ]; then
    echo -e "${RED}Error: Archive directory not found: $ARCHIVE_DIR${NC}"
    exit 1
fi

# Step 1: Scan for node_modules directories
echo -e "${YELLOW}Step 1: Scanning for node_modules directories...${NC}"
find "$ARCHIVE_DIR" -type d -name "node_modules" > "$TMP_LIST" 2>/dev/null

NODE_MODULES_COUNT=$(wc -l < "$TMP_LIST" | tr -d ' ')
echo -e "Found ${GREEN}$NODE_MODULES_COUNT${NC} node_modules directories\n"

if [ "$NODE_MODULES_COUNT" -eq 0 ]; then
    echo -e "${GREEN}No node_modules directories found. Archive is already clean!${NC}"
    rm -f "$TMP_LIST"
    exit 0
fi

# Step 2: Calculate total size
echo -e "${YELLOW}Step 2: Calculating total size...${NC}"
TOTAL_SIZE=$(du -ch $(cat "$TMP_LIST") 2>/dev/null | tail -1 | awk '{print $1}')
echo -e "Total size: ${GREEN}$TOTAL_SIZE${NC}\n"

# Step 3: Show sample of what will be deleted
echo -e "${YELLOW}Step 3: Sample of directories to be removed:${NC}"
head -10 "$TMP_LIST" | while IFS= read -r dir; do
    DIR_SIZE=$(du -sh "$dir" 2>/dev/null | awk '{print $1}')
    echo "  - $dir ($DIR_SIZE)"
done
if [ "$NODE_MODULES_COUNT" -gt 10 ]; then
    echo "  ... and $((NODE_MODULES_COUNT - 10)) more"
fi
echo ""

# Step 4: Dry run exit
if [ "$DRY_RUN" = true ]; then
    echo -e "${YELLOW}=== DRY RUN MODE ===${NC}"
    echo "No changes made. Run without --dry-run to actually delete."
    echo ""
    echo "Summary:"
    echo "  Directories to delete: $NODE_MODULES_COUNT"
    echo "  Total space to recover: $TOTAL_SIZE"
    rm -f "$TMP_LIST"
    exit 0
fi

# Step 5: Create backup if requested
if [ "$WITH_BACKUP" = true ]; then
    echo -e "${YELLOW}Step 4: Creating backup...${NC}"
    mkdir -p "$BACKUP_DIR"
    BACKUP_FILE="$BACKUP_DIR/archive_cleanup_$(date +%Y%m%d_%H%M%S).tar.gz"

    echo "Creating backup (excluding node_modules): $BACKUP_FILE"
    tar czf "$BACKUP_FILE" --exclude='node_modules' "$ARCHIVE_DIR" 2>/dev/null

    BACKUP_SIZE=$(du -sh "$BACKUP_FILE" | awk '{print $1}')
    echo -e "Backup created: ${GREEN}$BACKUP_SIZE${NC}"
    echo -e "Location: ${GREEN}$BACKUP_FILE${NC}\n"
fi

# Step 6: Confirm deletion
echo -e "${RED}WARNING: About to delete $NODE_MODULES_COUNT directories ($TOTAL_SIZE)${NC}"
echo -e "${YELLOW}This action cannot be undone (unless you created a backup).${NC}"
read -p "Continue? (yes/no): " CONFIRM

if [ "$CONFIRM" != "yes" ]; then
    echo -e "${YELLOW}Aborted by user.${NC}"
    rm -f "$TMP_LIST"
    exit 0
fi

# Step 7: Delete node_modules directories
echo -e "\n${YELLOW}Step 5: Deleting node_modules directories...${NC}"
DELETED=0
FAILED=0

while IFS= read -r dir; do
    if rm -rf "$dir" 2>/dev/null; then
        DELETED=$((DELETED + 1))
        if [ $((DELETED % 10)) -eq 0 ]; then
            echo "  Progress: $DELETED/$NODE_MODULES_COUNT deleted..."
        fi
    else
        FAILED=$((FAILED + 1))
        echo -e "  ${RED}Failed to delete: $dir${NC}"
    fi
done < "$TMP_LIST"

# Step 8: Summary
echo ""
echo -e "${GREEN}======================================"
echo "Cleanup Complete!"
echo -e "======================================${NC}"
echo ""
echo "Results:"
echo "  Deleted: ${GREEN}$DELETED${NC} directories"
if [ "$FAILED" -gt 0 ]; then
    echo "  Failed: ${RED}$FAILED${NC} directories"
fi
echo "  Space recovered: ${GREEN}$TOTAL_SIZE${NC}"
echo ""

if [ "$WITH_BACKUP" = true ]; then
    echo "Backup location: $BACKUP_FILE"
    echo "To restore: tar xzf $BACKUP_FILE -C ~/"
    echo ""
fi

# Verify depth reduction
echo -e "${YELLOW}Checking new folder depth...${NC}"
MAX_DEPTH=$(find "$ARCHIVE_DIR" -type d 2>/dev/null | awk -F'/' '{print NF}' | sort -rn | head -1)
echo "New maximum depth: ${GREEN}$MAX_DEPTH${NC} levels"
echo ""
echo -e "${GREEN}✅ Archive optimization complete!${NC}"

# Cleanup
rm -f "$TMP_LIST"
